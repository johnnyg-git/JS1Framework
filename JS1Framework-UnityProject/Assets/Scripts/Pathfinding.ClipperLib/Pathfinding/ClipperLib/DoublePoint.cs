namespace Pathfinding.ClipperLib
{
	public struct DoublePoint
	{
		public double X;

		public double Y;

		public DoublePoint(double x = 0.0, double y = 0.0)
		{
			X = 0.0;
			Y = 0.0;
		}

		public DoublePoint(DoublePoint dp)
		{
			X = 0.0;
			Y = 0.0;
		}

		public DoublePoint(IntPoint ip)
		{
			X = 0.0;
			Y = 0.0;
		}
	}
}
